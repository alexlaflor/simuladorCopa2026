function initMatches() {
  const result = {};
  Object.keys(GROUPS).forEach(gk => {
    result[gk] = GROUPS[gk].matches.map((m, i) => ({
      ...m,
      id: `${gk}_${i}`,
      // Jogos pendentes: hs/as = null, editável
      editHs: m.done ? null : "",
      editAs: m.done ? null : "",
    }));
  });
  return result;
}

